# ── Работа с оркестратором ────────────────────────────────────────────────────

_STRIP_DETAILS = re.compile(r"<details>.*?</details>", re.DOTALL)
_STRIP_SOURCES = re.compile(r"\nИсточники:.*$", re.DOTALL)
_STRIP_RELAY   = re.compile(r"\n🔄 Отправлен на проверку.*$", re.DOTALL)

def _clean_answer(text: str) -> str:
    text = _STRIP_DETAILS.sub("", text)
    text = _STRIP_SOURCES.sub("", text)
    text = _STRIP_RELAY.sub("", text)
    return text.strip()


def ask_qwen(question: str) -> dict | None:
    """Задать вопрос оркестратору. Возвращает dict или None при ошибке."""
    try:
        r = requests.post(
            ORCH_URL,
            json={"query": question, "enable_web": True},
            timeout=150,
            proxies={"http": None, "https": None},  # обход прокси
        )
        d = r.json()
        if d.get("is_clarification"):
            return None  # оркестратор попросил уточнения — пропускаем
        return d
    except Exception as e:
        print(f"  [Qwen error] {e}")
        return None


# ── Запись цепочки ────────────────────────────────────────────────────────────

def save_chain(record: dict) -> Path:
    filename = f"{record['id']}_{record['created_at'][:10]}.json"
    path = DATASET_DIR / filename
    path.write_text(json.dumps(record, ensure_ascii=False, indent=2), encoding="utf-8")
    return path


# ── Основной цикл ─────────────────────────────────────────────────────────────

def run(questions: list[str], pause: int = 10) -> dict:
    stats = {"total": 0, "skipped": 0, "verified": 0, "partial": 0,
             "supplemented": 0, "rejected": 0, "unverified": 0}

    for i, question in enumerate(questions, 1):
        print(f"\n[{i}/{len(questions)}] {question}")

        # Шаг 1 — Qwen
        print("  → Qwen...", end="", flush=True)
        qwen = ask_qwen(question)
        if qwen is None:
            print(" пропущено (уточнение)")
            stats["skipped"] += 1
            continue

        raw_answer = qwen.get("answer", "")
        answer     = _clean_answer(raw_answer)
        trust      = qwen.get("trust_level", "HYPOTHESIS")
        latency    = round(qwen.get("latency", 0), 1)
        print(f" {latency}s [{trust}]")

        # Шаг 2 — GPT-5.5
        print("  → GPT-5.5...", end="", flush=True)
        val = codex_validate(question, answer)
        verdict = val["verdict"]
        print(f" {verdict}")
        if val["correction"]:
            print(f"     ✗ {val['correction'][:120]}")
        if val["supplement"]:
            print(f"     + {val['supplement'][:120]}")

        steps = [
            {
                "step":    1,
                "role":    "initial_answer",
                "model":   "qwen",
                "text":    answer,
                "trust":   trust,
                "latency": latency,
            },
            {
                "step":          2,
                "role":          "critique",
                "model":         "gpt-5.5",
                "verdict":       verdict,
                "correction":    val["correction"],
                "supplement":    val["supplement"],
                "full_response": val["raw"],
            },
        ]

        # Шаг 3 — Qwen рефайн (если есть замечания)
        if verdict != "VERIFIED":
            print("  → Qwen рефайн...", end="", flush=True)
            refined = qwen_refine(
                question=question,
                initial_answer=answer,
                correction=val["correction"],
                supplement=val["supplement"],
                critic_model="GPT-5.5",
            )
            if refined["ok"]:
                print(f" {refined['latency']}s")
                steps.append({
                    "step":    3,
                    "role":    "refined_answer",
                    "model":   "qwen",
                    "text":    refined["text"],
                    "trust":   refined["trust"],
                    "latency": refined["latency"],
                })
            else:
                print(f" ошибка: {refined['error']}")

        # Цепочка
        record = {
            "id":         uuid.uuid4().hex[:8],
            "question":   question,
            "steps":      steps,
            "verdict":    verdict,
            "created_at": time.strftime("%Y-%m-%dT%H:%M:%S"),
        }

        path = save_chain(record)
        print(f"  ✓ сохранено: {path.name}")

        stats["total"] += 1
        key = verdict.lower().replace("partially_verified", "partial")
        if key in stats:
            stats[key] += 1

        if i < len(questions):
            time.sleep(pause)

    return stats


